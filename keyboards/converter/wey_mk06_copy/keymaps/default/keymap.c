// Turn on WS-specific layers.
void mk06_ws_activate(uint8_t ws) {
    static uint8_t current_ws = 0;
    if (ws < WS_MIN || ws > WS_MAX) {
        ws = 0;
    }
    if (current_ws != ws) {
        if (current_ws != 0) {
            layer_off(current_ws);
        }
        current_ws = ws;
        if (current_ws != 0) {
            layer_on(current_ws);
        }
    }
}
